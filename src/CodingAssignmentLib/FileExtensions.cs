namespace CodingAssignmentLib
{
    /// <summary>
    /// Class containing all supported file extensions by the file parsers.
    /// </summary>
    public static class FileExtensions
    {
        /// <summary>
        /// The extension for .csv files.
        /// </summary>
        public const string Csv = ".csv";

        /// <summary>
        /// The extension for .json files.
        /// </summary>
        public const string Json = ".json";

        /// <summary>
        /// The extension for .xml files.
        /// </summary>
        public const string Xml = ".xml";
    }
}